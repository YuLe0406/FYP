CREATE TABLE USER (
    U_ID INT AUTO_INCREMENT PRIMARY KEY,
    U_FName VARCHAR(255) NOT NULL,
    U_LName VARCHAR(255) NOT NULL,
    U_DOB VARCHAR(255) NOT NULL,
    U_Gender VARCHAR(255) NOT NULL,
    U_Email VARCHAR(255) NOT NULL,
    U_Password VARCHAR(255) NOT NULL,
    U_PNumber VARCHAR(11) NOT NULL,
    U_Address VARCHAR(255) NOT NULL
);

CREATE TABLE SUPERADMIN (
    SA_ID INT AUTO_INCREMENT PRIMARY KEY,
    SA_Name VARCHAR(255) NOT NULL,
    SA_Password VARCHAR(255) NOT NULL
);

CREATE TABLE ADMIN (
    A_ID INT AUTO_INCREMENT PRIMARY KEY,
    A_Name VARCHAR(255) NOT NULL,
    A_Password VARCHAR(255) NOT NULL
);

CREATE TABLE CATEGORIES (
    C_ID INT AUTO_INCREMENT PRIMARY KEY,
    C_Name VARCHAR(255) NOT NULL,
    C_Style VARCHAR(255) NOT NULL
);

CREATE TABLE PRODUCT (
    P_ID INT AUTO_INCREMENT PRIMARY KEY,
    C_ID INT NOT NULL,
    P_Name VARCHAR(255) NOT NULL,
    P_Price DECIMAL(10,2) NOT NULL,
    P_Color VARCHAR(255) NOT NULL,
    P_Size VARCHAR(255) NOT NULL,
    P_Quantity INT(11) NOT NULL,
    P_Picture BLOB NOT NULL,
    FOREIGN KEY (C_ID) REFERENCES CATEGORIES(C_ID)
);

CREATE TABLE ORDER (
    O_ID INT AUTO_INCREMENT PRIMARY KEY,
    U_ID INT NOT NULL,
    O_PM VARCHAR(50) NOT NULL,
    O_Date TIMESTAMP NOT NULL,
    O_Status VARCHAR(50) NOT NULL,
    FOREIGN KEY (U_ID) REFERENCES USER(U_ID)
);

CREATE TABLE Order_Items (
    OI_ID INT AUTO_INCREMENT PRIMARY KEY,
    O_ID INT NOT NULL,
    P_ID INT NOT NULL,
    O_Quantity INT(11) NOT NULL,
    O_I_Price DECIMAL(10,2) NOT NULL,
    FOREIGN KEY (O_ID) REFERENCES ORDER(O_ID),
    FOREIGN KEY (P_ID) REFERENCES PRODUCT(P_ID)
);